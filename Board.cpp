void Board::print(int lives, int score) const
{
	std::cout << board;

	printInfoLine(lives, score);
}

const char* Board::getBoard() const
{
	return board;
}

char Board::get(int x, int y) const
{
	return board[Utilities::WIDTH_OF_BOARD * y + x];
}

char Board::getPoint(const Point& p) const
{
	return board[Utilities::WIDTH_OF_BOARD * p.get_Y() + p.get_X()];
}

void Board::set(int x, int y, char ch)
{
	board[Utilities::WIDTH_OF_BOARD * y + x] = ch;
}

void Board::set(const Point& p, char ch)
{
	board[Utilities::WIDTH_OF_BOARD * p.get_Y() + p.get_X()] = ch;
}

void Board::printInfoLine(int lives, int score) const
{
	cout << "lives: " << lives << ", score: " << score <<  endl;
}